#include "clr.h"

t_clr	lerp_clr(t_clr const a)
{
	t_clr	b;

	b.r = ((a.r > 1.0) ? 1.0 : a.r) * 255.0;
	b.g = ((a.g > 1.0) ? 1.0 : a.g) * 255.0;
	b.b = ((a.b > 1.0) ? 1.0 : a.b) * 255.0;
	b.a = ((a.a > 1.0) ? 1.0 : a.a) * 255.0;
	return (b);
}
