#include <unistd.h>

void	ft_putstr_fd(char const *s, int fd)
{
	int		i;

	if (!s)
		return ;
	i = 0;
	while (s[i] != '\0')
		i++;
	while (i > 0)
	{
		write(fd, s, (i > 4096) ? 4096 : i);
		i -= (i > 4096) ? 4096 : i;
	}
}
