int		gl_match(char const *s1, char const *s2)
{
	unsigned int	i;
	unsigned int	j;

	i = 0;
	j = 0;
	while (s1[i] != '\0' && s2[j] != '\0')
	{
		if (s2[j] == '*')
		{
			while (s2[j] == '*')
				j++;
			j--;
		}
		if (s1[i] != '\0' && s2[j] == '*' && s2[j + 1] != s1[i])
			i++;
		else if (s1[i] != '\0' && s2[j] == '*')
			j++;
		else if (s1[i] == '\0' && s2[j] == '*')
			j++;
		else if (s1[i] == s2[j] || s2[j] == '?')
//		else if (s1[i] == s2[j] || (s1[i] != '\0' && s2[j] == '?'))
		{
			i++;
			j++;
		}
		else
			return (0);
	}
	while (s2[j] == '*' || s2[j] == '?')
		j++;
	return (s1[i] == s2[j]);
}
